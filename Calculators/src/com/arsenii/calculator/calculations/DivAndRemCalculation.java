package com.arsenii.calculator.calculations;

public class DivAndRemCalculation implements SpecificCalculation {
    
	@Override
	public String calculate(double first, double second) {
		long preAns = Math.round(first / second);
		long remainder = Math.round(first % second);
		String answer = (Double.toString(preAns) + ", R"+ remainder);
		return answer;
	}

}
